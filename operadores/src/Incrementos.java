public class Incrementos {
    public static void main(String[] args) {
        int numero = 5;

        System.out.println(numero ++); //5 + 1
        System.out.println(numero ++); //6 + 1
        System.out.println(numero ++); //7 + 1
        System.out.println(numero ++); //8 + 1
        System.out.println(numero --); //9 - 1
        System.out.println(numero --); //8 - 1
        System.out.println(numero --); //7 - 1
        System.out.println(numero --); //6 - 1
        System.out.println(numero);    // 5
    }
}
