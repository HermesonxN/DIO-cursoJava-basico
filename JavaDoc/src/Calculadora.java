public class Calculadora {
    /**
     * Este método e usado para somar dois números inteiros
     * @param numeroUm Este e o primeiro parametro do método
     * @param numeroDois Este e o segundo parametro do método
     * @return int o resultado deste método e a soma dos dois numeros
     */
    public int somar(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;
    }
}
